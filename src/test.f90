!==============================================================================================================
  subroutine fmean(n,vec,mean)
!==============================================================================================================

  use iso_c_binding

  implicit none

  integer(c_int) :: n, i
  real(c_double) :: vec(n),mean
 
  mean=0.0d0
  do i=1,n
    mean = mean + vec(i)
  enddo
  mean=mean/n
  end subroutine fmean
!==============================================================================================================
